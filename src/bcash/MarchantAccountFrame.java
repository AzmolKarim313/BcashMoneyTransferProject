    private void txtbKashNoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbKashNoKeyTyped
        // TODO add your handling code here:
        initialValues();
        char key =evt.getKeyChar();
        if (!(Character.isDigit(key) & (strMobile.length() < 11))) {
            evt.consume();
        }
    }//GEN-LAST:event_txtbKashNoKeyTyped

    private void txtAmountKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAmountKeyTyped
        // TODO add your handling code here:
        char key =evt.getKeyChar();
        if (!(Character.isDigit(key) || key == KeyEvent.VK_PERIOD)) {
            evt.consume();
        }
    }//GEN-LAST:event_txtAmountKeyTyped

    private void txtReferenceKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtReferenceKeyTyped
        // TODO add your handling code here:
        initialValues();
        char key =evt.getKeyChar();
        if (!(Character.isDigit(key) & (strReference.length() < 2))) {
            evt.consume();
        }
    }//GEN-LAST:event_txtReferenceKeyTyped

