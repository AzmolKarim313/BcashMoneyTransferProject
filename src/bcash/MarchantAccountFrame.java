    public void clearField(){
        txtbKashNo.setText("");
        txtAmount.setText("");
        txtReference.setText("");
        cbxDistination.setSelectedIndex(0);
        txtPass.setText("");
    }
    
    public void initialValues(){
        strMobile = txtbKashNo.getText();
        strAmount = txtAmount.getText();
        strReference = txtReference.getText();
        strDistination = cbxDistination.getSelectedItem().toString();
        strPass = txtPass.getText();
        
        try {
            amount = Float.parseFloat(strAmount);
        } catch (NumberFormatException e) {
        }
    }
    
    public  void inactiveField(){
        txtPass.setText("");
        txtPass.setEditable(false);
        btnSubmit.setEnabled(false);
    }
    
    public  void activeField(){
        txtPass.setEditable(true);
        btnSubmit.setEnabled(true);
    }
    
    public void payment(){
        initialValues();
        dbObj.doConnection();
        String stm = "update user_login_table set amount = amount - " + 
                amount + " where u_id = '" + LoginFrame.getStrMobile() + 
                "' and amount > " + amount +" ;";
        int n = dbObj.modifyData(stm);
        if (n > 0) {
            String paymentStm = "update user_login_table set amount = amount + " + 
                    amount + " where u_id = '" + strMobile + "';";
            int m = dbObj.modifyData(paymentStm);
            if (m > 0) {
                String transStm = "insert into transaction_table(s_id,r_id,amount) values ('" + LoginFrame.getStrMobile() + "', '" + strMobile + "', " + amount + ");";
                int t = dbObj.modifyData(transStm);
                if (t > 0) {
                    JOptionPane.showMessageDialog(null, "Your can given payment successfully");
                }
            } else {
                String reverseStm = "update user_login_table set amount = amount + " + 
                        amount + " where u_id = '" + LoginFrame.getStrMobile() + "' ;";
                int p = dbObj.modifyData(reverseStm);
                JOptionPane.showMessageDialog(null, "Marchant bKask account invalid !!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Your have no enough suffecent balance.");
        }
    }

